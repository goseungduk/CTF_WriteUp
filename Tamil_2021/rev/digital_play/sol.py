from binascii import *

ciph = ["00110110111111100000011000101", "100001000000100000011000010101", "001001111110101001110011001011", "1111100000101010000110100010000",
        "011011111011001110111011011001", "1111100000101010110011100001100", "010011111011001100100011110011", "1100001101100001011101100110", "0000010111100111100100011010001"]

flag = ""

for i in range(0, len(ciph)):
    ciph1 = ciph[i]
    if(i % 2 == 0):
        part = hex((int(ciph1, 2) ^ ((1 << len(ciph1))-1)) ^ 0x4d415253)
    else:
        part = hex(int(ciph1, 2) ^ 0x4d415253)
    flag += unhexlify(part[2:]).decode()

print(flag)
